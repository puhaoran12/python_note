# 格式化字符串
# 1.%作占位符  %s字符串  %i或者%d整数  %f浮点数
name='张三'
age=18
ac=100
print('我叫%s,今年%d岁——%s' %(name,age,name))#前面有几个%，后面括号中就有几个参数，且顺序要一致
# 2.{}作占位符
print('我叫{0},今年{1}岁——{0}'.format(name,age,ac))#花括号{}中参数是format()中参数的索引
#3.f-string
print(f'我叫{name}，今年{age}岁——{name}')


print('%10d' % 99)#10表示的是总的宽度
print('%.3f' % 3.1415926)#.3表示的是小数点后三位数
#同时表示宽度和进度
print('%10.3f' % 3.1415926)#总宽度为10，小数点后三位数